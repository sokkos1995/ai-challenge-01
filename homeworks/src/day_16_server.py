from mcp.server.fastmcp import FastMCP


# Создаем локальный MCP-сервер.
# MCP-сервер - это программа, которая "отдает наружу" инструменты.
# Клиент может подключиться к нему, спросить "какие инструменты у тебя есть?",
# а потом при необходимости вызвать любой из них.
server = FastMCP(
    name="day-16-local-server",
    log_level="ERROR",
    instructions=(
        "Учебный локальный MCP-сервер для домашнего задания. "
        "Показывает, как клиент подключается к серверу и получает список инструментов."
    ),
)


@server.tool()
def hello(name: str = "world") -> str:
    """Возвращает простое приветствие."""
    return f"Hello, {name}!"


@server.tool()
def add(a: int, b: int) -> int:
    """Складывает два числа и возвращает результат."""
    return a + b


if __name__ == "__main__":
    # Важно:
    # transport="stdio" значит, что сервер общается с клиентом через stdin/stdout.
    # Это самый простой способ для локального примера:
    # клиент просто запускает этот Python-файл как подпроцесс.
    #
    # Еще один важный момент для новичка:
    # когда сервер работает через stdio, нельзя делать print() в stdout для отладки,
    # потому что stdout занят служебным MCP-протоколом.
    server.run(transport="stdio")
